package controle;

public class WhileDeterminado {
/*
 * while é uma estrutura para utilização inderteminada, para utlização deteminada o ideal é usa
 * */
	public static void main(String[] args) {
		
		int contador = 1;
		
		while(contador <=10) {
			System.out.println("Bom dia! " + contador);
			contador++;
		}
	}
}
